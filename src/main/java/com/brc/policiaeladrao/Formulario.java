    private void btLadraoCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLadraoCriarActionPerformed
        if (txtLadraoNome.getText().equals("") || 
                txtLadraoVeiculo.getText().equals("")||
                txtLadraoComplexo.getText().equals("")) {
            
            JOptionPane.showMessageDialog(null, "Crie o ladrão :p");
            return;
        }
        ladrao = new Ladrao(
                txtLadraoNome.getText(), 
                txtLadraoVeiculo.getText(), 
                txtLadraoComplexo.getText());
        
        btLadraoAtirar.setEnabled(true);
        btLadraoFuga.setEnabled(true);
        btLadraoArregar.setEnabled(true);
        btLadraoSequestrar.setEnabled(true);
        mutLadraoVida.setText(String.valueOf(ladrao.vidaAtual) + "%");
        btLadraoCriar.setEnabled(false);
        imgLula.setVisible(true);
        txtLadraoImgNome.setText("nome: "+ ladrao.nome);
        txtLadraoImgVeiculo.setText("veiculo: " + ladrao.veiculoDeFuga);
        txtLadraoImgComplexo.setText("complexo: " + ladrao.complexo);
        txtLadraoNome.setText("");
        txtLadraoVeiculo.setText("");
        txtLadraoComplexo.setText("");
    }//GEN-LAST:event_btLadraoCriarActionPerformed

    private void btPoliciaAtirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPoliciaAtirarActionPerformed
        if (ladrao == null) {
            JOptionPane.showMessageDialog(null,"Não tem ninguém pra você atirar..");
            return;
        }
        SecureRandom random = new SecureRandom();
        mutUpdatePanel.setText(policia.atirar(ladrao));
        mutLadraoVida.setText(String.valueOf(ladrao.vidaAtual));
        if (ladrao.vidaAtual == 0) {
            JOptionPane.showMessageDialog(null,String.format("%s FOI DE BASE. RIP", ladrao.nome));
            btLadraoArregar.setEnabled(false);
            btLadraoAtirar.setEnabled(false);
            btLadraoFuga.setEnabled(false);
            btLadraoSequestrar.setEnabled(false);
            btPoliciaArregar.setEnabled(false);
            btPoliciaAtirar.setEnabled(false);
            btPoliciaPrender.setEnabled(false);
            btPoliciaReforcos.setEnabled(false);
            btResetar.setEnabled(true);
        }
        Color[] cores = {Color.RED, Color.DARK_GRAY, Color.cyan};
        mutLadraoVida.setForeground(cores[random.nextInt(cores.length - 1)]);
    }//GEN-LAST:event_btPoliciaAtirarActionPerformed

