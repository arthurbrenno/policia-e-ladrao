    private void btPoliciaCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPoliciaCriarActionPerformed
        if (txtPoliciaNome.getText().equals("") || txtPoliciaRadio.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Crie o policial rapá");
            return;
        }
        policia = new Policia(
                txtPoliciaNome.getText(), 
                alg, 
                txtPoliciaRadio.getText());
        btPoliciaAtirar.setEnabled(true);
        btPoliciaPrender.setEnabled(true);
        btPoliciaReforcos.setEnabled(true);
        btPoliciaArregar.setEnabled(true);
        mutPoliciaVida.setText(String.valueOf(policia.vidaAtual) + "%");
        btAlgemaFalse.setEnabled(false);
        btAlgemaTrue.setEnabled(false);
        btPoliciaCriar.setEnabled(false);
        txtPoliciaNome.setText("");
        txtPoliciaRadio.setText("");
        txtPoliciaImgNome.setText("nome: "+ policia.nome);
        txtPoliciaImgRadio.setText("radin: " + policia.radio);
        imgTerry.setVisible(true);
        if (alg) {
            txtPoliciaImgAlgemas.setText("a l g e m a d o");
        }
        else {
            txtPoliciaImgAlgemas.setText("Sem as algemas");
        }
    }//GEN-LAST:event_btPoliciaCriarActionPerformed

